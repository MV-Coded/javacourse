public class Main {
    public static void main(String[] args) {
        // Вариант 1: Инициализация в объявлении (прямое присваивание)
        // Это самый простой и распространенный способ.
        public final int a = 10;

        // Вариант 2: Инициализация в конструкторе класса
        // Константа объявляется как поле, а значение присваивается в конструкторе.
        // Это позволяет вычислить значение при создании объекта.
        public final int b;
        b = calculateValue(); // Пример вычисления значения

        // Вариант 3: Инициализация в блоке инициализации (instance initializer block)
        // Блок выполняется перед конструктором при создании объекта.
        // Полезно, если логика инициализации сложная или повторяется.
        public final int c;
        {
            c = 42; // Простая инициализация в блоке
        }

        // Вариант 4: Инициализация в статическом блоке (для static final переменных)
        // Статические блоки выполняются при загрузке класса.
        // Этот вариант подходит только для static final полей.
        // public static final int d; // Объявление должно быть static
        // static { d = 100; } // Инициализация в статическом блоке

        // Важно: Все эти варианты гарантируют, что константа получит значение
        // ровно один раз до того, как на нее будет сделана первая ссылка.
        // После инициализации значение изменить нельзя.

        // Пример использования (для демонстрации):
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }

    // Вспомогательный метод для примера
    private static int calculateValue() {
        return 5 * 5; // Возвращает 25
    }
}

public class Main {
    public static void main(String[] args) {

        // Пример бесконечного цикла while
        int counterWhile = 0;
        while (true) { // Условие всегда истинно
            counterWhile++; 
	}

        // Пример бесконечного цикла do-while
        System.out.println("\nБесконечный цикл do-while:");
        int counterDoWhile = 0;
        do {
            counterDoWhile++;
        } while (true); // Условие всегда истинно
    }
}

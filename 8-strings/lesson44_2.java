public class Main {
    public static void main(String[] args) {
        // Метод flush() нужен для принудительной отправки (сброса) данных из буфера вывода.
        // Он гарантирует, что все данные, накопленные в буфере, будут немедленно записаны в целевой поток (например, консоль или файл),
        // даже если буфер не заполнен полностью.
        // Это важно при работе с потоками, где нужно убедиться, что пользователь или другая система получит данные сразу.
        
        System.out.print("Пример текста без перевода строки");
        System.out.flush(); // Сбрасываем буфер, чтобы текст отобразился немедленно
        System.out.println(" - продолжение после flush()");
    }
}

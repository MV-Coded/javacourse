public class Main {
    public static void main(String[] args) {
        // Три двойные кавычки (""") используются для создания текстовых блоков (text blocks)
        // Начиная с Java 15 (preview), а с Java 17 — стабильно.
        // Они позволяют писать многострочные строки без необходимости экранирования.

        String oldWay = "Первая строка\n" +
                        "Вторая строка\n" +
                        "Третья строка с \"кавычками\" внутри.";

        String newWay = """
                Первая строка
                Вторая строка
                Третья строка с "кавычками" внутри.
                """;

        System.out.println("Старый способ (с экранированием):");
        System.out.println(oldWay);
        System.out.println("\nНовый способ (текстовый блок):");
        System.out.println(newWay);

        //Преимущества: Автоматическое форматирование 
	//Не нужно экранировать кавычки и переносы
	//Удобно для SQL, JSON, HTML и других многострочных данных

    }
}

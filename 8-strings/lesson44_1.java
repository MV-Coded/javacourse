public class Main {
    public static void main(String[] args) {
        boolean bool = true;
        int integer = 42;
        double floating = 3.14159;
        String text = "Hello, World!";
        char symbol = 'A';

        System.out.printf("Логическое: %b%n", bool);
        System.out.printf("Целое: %d%n", integer);
        System.out.printf("Плавающая точка: %f%n", floating);
        System.out.printf("Строка: %s%n", text);
        System.out.printf("Символ: %c%n", symbol);
    }
}
